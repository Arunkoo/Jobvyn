"use client";
import { jobs } from "@/type";
import React, { useEffect, useRef, useState } from "react";
import Cookies from "js-cookie";
import axios from "axios";
import { job_service_url } from "@/context/AppContext";
import { Button } from "@/components/ui/button";
import { Briefcase, FilterIcon, MapPin, Search, X } from "lucide-react";
import Loading from "@/components/loading";
import JobsCard from "@/components/jobsCard";
import {
  Dialog,
  DialogContent,
  DialogFooter,
  DialogHeader,
  DialogTitle,
  DialogTrigger,
} from "@/components/ui/dialog";
import { Label } from "@/components/ui/label";
import { Input } from "@/components/ui/input";
import { Select } from "@/components/ui/select";
const location: string[] = [
  "Delhi",
  "Mumbai",
  "Bengluru",
  "Hyderabad",
  "Pune",
  "Kolkata",
  "Chennai",
  "Remote",
];
const JobsPage = () => {
  const [loading, setLoading] = useState(true);
  const [jobs, setJobs] = useState<jobs[]>([]);
  const [title, setTitle] = useState("");
  const [Location, setLocation] = useState("");
  const token = Cookies.get("token");
  const ref = useRef<HTMLButtonElement>(null);
  const clickEvent = () => {
    ref.current?.click();
  };
  const clearFilter = () => {
    setTitle("");
    setLocation("");
    fetchJobs();
    ref.current?.click();
  };

  const hasFilterActive = title || Location;
  async function fetchJobs() {
    setLoading(true);
    try {
      const { data } = await axios.get(
        `${job_service_url}/api/job/all?title=${title}&location=${Location}`,
        { headers: { Authorization: `Bearer ${token}` } },
      );
      console.log(data);
      setJobs(data);
    } catch (error) {
      console.log(error);
    } finally {
      setLoading(false);
    }
  }
  useEffect(() => {
    fetchJobs();
  }, [title, Location]);
  return (
    <div className="min-h-screen bg-secondary/30">
      <div className="max-w-7xl mx-auto px-4 py-8">
        {/* Header Section */}
        <div className="mb-8">
          <div className="flex items-center justify-between flex-wrap gap-4 mb-4">
            <div>
              <h1 className="text-3xl md:text-4xl font-bold mb-2">
                Explore <span className="text-red-500">opportunities</span>
              </h1>
              <p className="text-base opacity-70 ">{jobs.length} jobs</p>
            </div>
            <Button className="gap-2 h-11" onClick={clickEvent}>
              <FilterIcon size={18} />
              Filters
              {hasFilterActive && (
                <span className="ml-1 px-2 py-0.5 rounded-full bg-red-500 text-white text-xs">
                  Active
                </span>
              )}
            </Button>
          </div>
          {hasFilterActive && (
            <div className="flex items-center gap-2 flex-wrap">
              <span className="text-sm opacity-70">Active Filters:</span>
              {title && (
                <div className="flex items-center gap-2 px-3 py-1.5 rounded-full bg-blue-100 dark:bg-lue-900/30 text-blue-600 text-sm">
                  <Search size={14} />
                  {title}
                  <button
                    title="abort"
                    onClick={() => setTitle("")}
                    className="bg-blue-200 dark:bg-blue-800 rounded-full p-0.5"
                  >
                    <X size={14} />
                  </button>
                </div>
              )}
              {Location && (
                <div className="flex items-center gap-2 px-3 py-1.5 rounded-full bg-blue-100 dark:bg-lue-900/30 text-blue-600 text-sm">
                  <MapPin size={14} />
                  {Location}
                  <button
                    title="abort"
                    onClick={() => setLocation("")}
                    className="bg-blue-200 dark:bg-blue-800 rounded-full p-0.5"
                  >
                    <X size={14} />
                  </button>
                </div>
              )}
            </div>
          )}
          {loading ? (
            <Loading />
          ) : (
            <>
              {jobs && jobs.length > 0 ? (
                <div className="grid grid-col-1 md:grid-cols-2 lg:grid-cols-3 gap-6 mb-8">
                  {jobs.map((j) => (
                    <JobsCard key={j.job_id} job={j} />
                  ))}
                </div>
              ) : (
                <div className="text-center py-16">
                  <div className="inline-flex items-center justify-center w-20 h-20 rounded-full bg-gray dark:bg-gray-800 mb-4">
                    <Briefcase size={40} className=" opacity-40" />
                  </div>
                  <h3 className="text-xl font-semibold mb-2">No jobs found</h3>
                </div>
              )}
            </>
          )}
        </div>
        <Dialog>
          <DialogTrigger asChild>
            <Button ref={ref} className="hidden"></Button>
          </DialogTrigger>
          <DialogContent className="sm:max-w-125">
            <DialogHeader>
              <DialogTitle className="text-2xl flex items-center gap-2">
                <FilterIcon className="text-blue-600" />
                Filter Jobs
              </DialogTitle>
            </DialogHeader>
            <div className="space-y-3 py-3">
              <div className="space-y-1.5">
                <Label htmlFor="title" className="text-sm">
                  <Search size={16} />
                  Search by job title
                </Label>
                <Input
                  id="title"
                  value={title}
                  onChange={(e) => setTitle(e.target.value)}
                  className="h-9 text-sm"
                />
              </div>
              <div className="space-y-1.5">
                <Label htmlFor="location" className="text-sm">
                  <MapPin size={16} />
                  Search by job location
                </Label>
                <select
                  title="location"
                  id="location"
                  value={Location}
                  onChange={(e) => setLocation(e.target.value)}
                  className="w-full h-11 px-3 border-2 border-gray-300 rounded-md bg-transparent focus:outline-none focus:ring-2"
                >
                  <option value="">All locations</option>
                  {location.map((e) => (
                    <option value={e} key={e}>
                      {e}
                    </option>
                  ))}
                </select>
              </div>
            </div>

            <DialogFooter className="gap-2">
              <Button
                variant={"outline"}
                onClick={clearFilter}
                className="flex-1"
              >
                Clear All
              </Button>
            </DialogFooter>
          </DialogContent>
        </Dialog>
      </div>
    </div>
  );
};

export default JobsPage;
